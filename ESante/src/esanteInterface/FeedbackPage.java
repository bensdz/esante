public void insertFeedback() {
    String query = "INSERT INTO feedback VALUES (?, ?, ?, ?, ?, ?)";
    service1.setActionCommand("1");
    service2.setActionCommand("2");
    service3.setActionCommand("3");
    service4.setActionCommand("4");
    service5.setActionCommand("5");

    ponct1.setActionCommand("1");
    ponct2.setActionCommand("2");
    ponct3.setActionCommand("3");
    ponct4.setActionCommand("4");
    ponct5.setActionCommand("5");

    traitem1.setActionCommand("1");
    traitem2.setActionCommand("2");
    traitem3.setActionCommand("3");
    traitem4.setActionCommand("4");
    traitem5.setActionCommand("5");

    try (Connection conn = getConnection();
         PreparedStatement pstmt = conn.prepareStatement(query)) {

        pstmt.setInt(1, Integer.parseInt(consultIDField.getText()));
        pstmt.setString(2, commentField.getText());
        pstmt.setInt(3, Integer.parseInt(serviceRating.getSelection().getActionCommand()));
        pstmt.setInt(4, Integer.parseInt(ponctRating.getSelection().getActionCommand()));
        pstmt.setInt(5, Integer.parseInt(traitementRating.getSelection().getActionCommand()));
        pstmt.setInt(6, Integer.parseInt(consultIDField.getText()));

        pstmt.executeUpdate();
        JOptionPane.showMessageDialog(null, "Record inserted successfully!");
    } catch (SQLIntegrityConstraintViolationException e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(null, "Feedback for the same consultID already exists in the database.");
    } catch (SQLException e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(null, "An error occurred while inserting the feedback record.");
    }
}


