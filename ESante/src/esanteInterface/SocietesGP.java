    public ArrayList<Societe> societeList () {
      ArrayList<Societe> reList = new ArrayList<>();
        try {
            Connection conn = (Connection) MySQLConnection.getConnection();
            String query="SELECT * From Societe";
            Statement st= conn.createStatement();
            ResultSet rs=st.executeQuery(query);
            Societe reser;
            while (rs.next()){
                reser= new Societe(rs.getInt("societeId"),rs.getString("adresse"),rs.getString("siteWeb"), rs.getInt("nCCP"));
                reList.add(reser);
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(SocietesGP.class.getName()).log(Level.SEVERE, null, ex);
        }
      return reList;
    } 
    
    public ArrayList<nTlphs> tlphsList () {
      ArrayList<nTlphs> ntlphList = new ArrayList<>();
        try {
            Connection conn = (Connection) MySQLConnection.getConnection();
            String query="SELECT * From nTlphs";
            Statement st= conn.createStatement();
            ResultSet rs=st.executeQuery(query);
            nTlphs nltph;
            while (rs.next()){
                nltph= new nTlphs(rs.getInt("nTlph"));
                ntlphList.add(nltph);
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(SocietesGP.class.getName()).log(Level.SEVERE, null, ex);
        }
      return ntlphList;
    } 
    
    public void showSocietes (){
        ArrayList<Societe> list = societeList();
        ArrayList<nTlphs> listlph=tlphsList();
        DefaultTableModel model = (DefaultTableModel) societeTable.getModel();
        Object[] row= new Object[7];
        for (int i=0;i<list.size();i++) {
           row[0]=list.get(i).getsocieteId();
           row[1]=list.get(i).getAdresse();
           row[2]=list.get(i).getSiteWeb();
           row[3]=list.get(i).getNCCP();
           row[4]=listlph.get(i).getnTlph();
           row[5]=listlph.get(i).getnTlph();
           row[6]=listlph.get(i).getnTlph();
           model.addRow(row);
        }
    }

